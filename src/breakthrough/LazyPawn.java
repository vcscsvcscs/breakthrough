import java.awt.Color;

/**
 *
 * @author v5inla
 */
public class LazyPawn {

    private Color color;

    public LazyPawn() {
        color = null;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
}
